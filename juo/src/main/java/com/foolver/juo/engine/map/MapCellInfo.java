package com.foolver.juo.engine.map;

public class MapCellInfo {

  private short tileId;

  private byte altitude;

  public short getTileId() {
    return tileId;
  }

  public void setTileId(short tileId) {
    this.tileId = tileId;
  }

  public byte getAltitude() {
    return altitude;
  }

  public void setAltitude(byte altitude) {
    this.altitude = altitude;
  }

}
